export const LABELS_BY_LANGUAGE: Record<UiLanguage, LabelDictionary> = {
  es,
  en,
  fr,
  de,
  it,
};

export type LabelKey = keyof typeof es;

export function resolveLabel(language: UiLanguage, key: LabelKey): string {
  const dictionary = LABELS_BY_LANGUAGE[language] ?? es;
  return dictionary[key] ?? es[key] ?? key;
}

export function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) {
    return template;
  }
  return Object.entries(params).reduce((acc, [paramKey, value]) => {
    return acc.split(`{${paramKey}}`).join(String(value));
  }, template);
}
